# "1010" 和 "10.10"：

# 比较两个字符串的第一个字符：'1' 和 '1'，相同。
# 比较第二个字符：'0' 和 '0'，相同。
# 比较第三个字符：'1' 和 '.'，由于 '1' 的 ASCII 值（49）大于 '.' 的 ASCII 值（46），所以 "1010" 大于 "10.10"。
# 比较 "1010" 和 "python"：

# 比较第一个字符：'1' 和 'p'，由于 '1' 的 ASCII 值（49）小于 'p' 的 ASCII 值（112），所以 "1010" 小于 "python"。

# list()----将变量x转换为列表数据类型
print(list("python"))



# 将字典转换为列表

# 字典 是一个可迭代对象，但 list() 函数只会提取字典的键，创建一个包含这些键的列表。
print(list({"201801":"小明", "201802":"小红", "201803":"小新"}))



