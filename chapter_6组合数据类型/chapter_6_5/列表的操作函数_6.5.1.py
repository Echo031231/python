# 一些通用的操作函数

# len()----列表中元素个数
ls = [1010, "1010", [1010, "1010"], 1010]
print(len(ls))


# max()   min()----列表中元素可以比较才可以使用
ls = [1010, 10.10, 0x1010]
print(min(ls))
print(max(ls))

lt = ["1010", "10.10", "python"]
print(max(lt))

